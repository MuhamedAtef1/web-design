var degree = prompt("Enter Your Degree")
if (degree > -1 && degree < 101 && degree != null && degree != "") {
    if (degree > 90) alert("Your Grade is A")
    else if (degree > 90) alert("Your Grade is A")
    else if (degree > 80) alert("Your Grade is B")
    else if (degree > 70) alert("Your Grade is C")
    else alert("Your Grade is F")
}
else {
    alert("Wrong degree")
}